%% 拟合-非线性回归模型(nlm)
% Nonlinear regression model
% 本脚本使用非线性最小二乘法拟合血药浓度数据，采用Weibull-like模型
%
% 作者: CodeBuddy
% 日期: 2026-04-05
% 功能: 对时间-血药浓度数据进行非线性回归建模和可视化

%% 1. 数据准备 - 定义时间序列和对应的血药浓度观测值

% time: 采样时间点（单位：小时），共50个观测值
time = [ 0.1   0.1   0.3   0.3   1.3   1.7   2.1   2.6   3.9   3.9 ...
         5.1   5.6   6.2   6.4   7.7   8.1   8.2   8.9   9.0   9.5 ...
         9.6  10.2  10.3  10.8  11.2  11.2  11.2  11.7  12.1  12.3 ...
        12.3  13.1  13.2  13.4  13.7  14.0  14.3  15.4  16.1  16.1 ...
        16.4  16.4  16.7  16.7  17.5  17.6  18.1  18.5  19.3  19.7]';

% conc: 对应时间点的血药浓度（单位：mg/L或其他浓度单位）
conc = [0.01  0.08  0.13  0.16  0.55  0.90  1.11  1.62  1.79  1.59 ...
        1.83  1.68  2.09  2.17  2.66  2.08  2.26  1.65  1.70  2.39 ...
        2.08  2.02  1.65  1.96  1.91  1.30  1.62  1.57  1.32  1.56 ...
        1.36  1.05  1.29  1.32  1.20  1.10  0.88  0.63  0.69  0.69 ...
        0.49  0.53  0.42  0.48  0.41  0.27  0.36  0.33  0.17  0.20]';

%% 2. 数据可视化 - 绘制原始观测值散点图

figure;  % 创建新的图形窗口
plot(time, conc, 'o');  % 以圆形标记绘制散点图
xlabel('Time');  % 设置X轴标签：时间
ylabel('Blood Concentration');  % 设置Y轴标签：血药浓度
title('血药浓度随时间变化趋势');  % 设置图形标题
grid on;  % 显示网格线

%% 3. 非线性模型定义与拟合

% 定义Weibull-like非线性模型函数
% 模型形式: y = p(3) * (x/p(1))^(p(2)-1) * exp(-(x/p(1))^p(2))
% 参数说明:
%   p(1) - 尺度参数(scale): 控制分布的伸展程度
%   p(2) - 形状参数(shape): 控制分布的形状特征
%   p(3) - 幅度参数(amplitude): 控制曲线的峰值高度
modelFun = @(p, x) p(3) .* (x./p(1)).^(p(2)-1) .* exp(-(x./p(1)).^p(2));

% 设置初始参数值 [尺度, 形状, 幅度]
% 这些初始值影响拟合收敛速度和结果，需要根据数据特点合理选择
startingVals = [10 2 5];

% 使用fitnlm函数进行非线性最小二乘拟合
% 输入参数: 自变量(time), 因变量(conc), 模型函数, 初始参数值
nlModel = fitnlm(time, conc, modelFun, startingVals);

% 显示拟合结果摘要
disp('拟合结果摘要:');
disp(nlModel);

%% 4. 绘制拟合曲线

% 生成用于绘制平滑曲线的密集网格点
xgrid = linspace(0, 20, 100)';

% 使用拟合模型预测网格点的浓度值，并绘制红色拟合曲线
line(xgrid, predict(nlModel, xgrid), 'Color', 'r', 'LineWidth', 2);
legend('观测数据', '拟合曲线', 'Location', 'best');

% 提取拟合参数
fittedParams = nlModel.Coefficients.Estimate;
fprintf('\n拟合参数:\n');
fprintf('  尺度参数 (p1): %.4f\n', fittedParams(1));
fprintf('  形状参数 (p2): %.4f\n', fittedParams(2));
fprintf('  幅度参数 (p3): %.4f\n', fittedParams(3));
